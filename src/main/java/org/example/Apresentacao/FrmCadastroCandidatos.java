    private void btn_movimentarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_movimentarActionPerformed
        //Verificações dos formulário
        boolean verifica = true;
        String erro = "";

        if (txt_numero.getText().trim().equals("0000")) {
            System.out.println("teste");
            verifica = false;
            erro = "Não pode criar um candidato com esse número, tente outro";
        }

        if ((txt_numero.getText().length() != 4) && verifica) {
            verifica = false;
            erro = "O número do candidato deve ter 4 caractéres";
        }

        if ((txt_nome.getText().length() < 5 || txt_nome.getText().length() > 15) && verifica) {
            verifica = false;
            erro = "O nome deve ter no mínimo 5 e no máximo 15 caractéres";
        }

        if ((txt_cargo.getText().length() < 5 || txt_cargo.getText().length() > 15) && verifica) {
            verifica = false;
            erro = "O cargo deve ter no mínimo 5 e no máximo 15 caractéres";
        }


        if (verifica) {
            try {
                Boolean result = c.adiciona_candidato(
                        FrmCadastroCandidatos.txt_nome.getText(),
                        FrmCadastroCandidatos.txt_cargo.getText(),
                        FrmCadastroCandidatos.txt_numero.getText(),
                        caminho_imagem
                );
                preenche_lista();

                if (result) {
                    JOptionPane.showMessageDialog(null, "Número de candidato já cadastrado");
                } else {
                    JOptionPane.showMessageDialog(null, "Candidato cadastrado!");
                    limpa_campos();
                }
            } catch (SQLException ex) {
                Logger.getLogger(FrmCadastroCandidatos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(FrmCadastroCandidatos.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, erro);
        }


    }//GEN-LAST:event_btn_movimentarActionPerformed

