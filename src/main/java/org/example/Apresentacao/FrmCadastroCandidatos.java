    public class CreateListCellRenderer extends DefaultListCellRenderer {
        public Component getListCellRendererComponent(
                JList list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
            // I know DefaultListCellRenderer always returns a JLabel
            // super setups up all the defaults
            JLabel label = (JLabel) super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);

            if (value instanceof Candidato) {
                Candidato course = (Candidato) value;

                label.setText(course.getNome() + " - " + course.getDs_numero());
            }


            return label;

        }
    }


